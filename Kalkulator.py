#Napisz program który wczyta dwie liczby wprowadzone
#przez użytkownika. Następnie wykonaj operacje:
#dodawania, odejmowania, mnożenia oraz dzielenia
#podanych liczb. Po wykonaniu każdej operacji wypisz jej wynik.
print("Podaj pierwszą liczbę: ")
a = int(input())
print("Podaj drugą liczbę: ")
b = int(input())
dodawanie = a + b
print("Wynikiem dodawania jest ")
print(dodawanie)
odejmowanie = a - b
print("Wynikiem odejmowania jest ")
print(odejmowanie)
mnozenie = a * b
print("Wynikiem mnożenia jest ")
print(mnozenie)
dzielenie = a / b
print("Wynikiem dzielenia jest ")
print(dzielenie)

